function listClick1(){
    if($('.list1').css('display')=='block'){
        $('.list1').css('display','none');
        $('.li1').css('background-color','#343d47');
    }else{
        $('.list1').css('display','block');
        $('.li1').css('background-color','#424f5f');
    }
}
